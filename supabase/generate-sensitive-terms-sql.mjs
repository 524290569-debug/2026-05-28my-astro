import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");
const sourceDir = path.resolve(repoRoot, process.argv[2] || "敏感词库");
const outputFile = path.resolve(
  repoRoot,
  process.argv[3] || "supabase/megastructure-sensitive-terms.generated.sql"
);

const rejectCategoryPatterns = [
  /广告/,
  /色情/,
  /暴恐/,
  /涉枪/,
  /涉爆/,
  /非法网址/
];

const highRiskCategoryPatterns = [
  /反动/,
  /政治/,
  /GFW/i,
  /贪腐/
];

const sqlString = (value) => `'${String(value).replaceAll("'", "''")}'`;

const normalizeTerm = (value) =>
  String(value)
    .replace(/^\uFEFF/, "")
    .trim()
    .toLowerCase();

const categoryAction = (category) =>
  rejectCategoryPatterns.some((pattern) => pattern.test(category)) ? "rejected" : "pending";

const categorySeverity = (category, action) => {
  if (action === "rejected") {
    return "0.92";
  }

  return highRiskCategoryPatterns.some((pattern) => pattern.test(category)) ? "0.78" : "0.65";
};

const chunk = (items, size) => {
  const chunks = [];

  for (let index = 0; index < items.length; index += size) {
    chunks.push(items.slice(index, index + size));
  }

  return chunks;
};

const files = (await readdir(sourceDir, { withFileTypes: true }))
  .filter((entry) => entry.isFile() && entry.name.toLowerCase().endsWith(".txt"))
  .map((entry) => entry.name)
  .sort((left, right) => left.localeCompare(right, "zh-Hans-CN"));

const rows = [];
const seen = new Set();
const skipped = {
  blank: 0,
  short: 0,
  long: 0,
  duplicate: 0
};

for (const fileName of files) {
  const filePath = path.join(sourceDir, fileName);
  const category = path.basename(fileName, path.extname(fileName));
  const action = categoryAction(category);
  const severity = categorySeverity(category, action);
  const content = await readFile(filePath, "utf8");

  for (const rawLine of content.split(/\r?\n/)) {
    const term = rawLine.replace(/^\uFEFF/, "").trim();
    const normalizedTerm = normalizeTerm(term);

    if (!normalizedTerm) {
      skipped.blank += 1;
      continue;
    }

    if (normalizedTerm.length < 2) {
      skipped.short += 1;
      continue;
    }

    if (normalizedTerm.length > 120) {
      skipped.long += 1;
      continue;
    }

    const key = `${category}\0${normalizedTerm}`;

    if (seen.has(key)) {
      skipped.duplicate += 1;
      continue;
    }

    seen.add(key);
    rows.push({
      category,
      term,
      normalizedTerm,
      action,
      severity,
      sourceFile: fileName
    });
  }
}

const lines = [
  "-- Generated by supabase/generate-sensitive-terms-sql.mjs.",
  "-- Execute supabase/megastructure-comments.sql before this file.",
  "-- This file contains the local sensitive dictionary and should not be committed.",
  "",
  "begin;",
  "",
  "truncate table public.megastructure_sensitive_terms;",
  ""
];

for (const group of chunk(rows, 500)) {
  lines.push(
    "insert into public.megastructure_sensitive_terms",
    "  (category, term, normalized_term, action, severity, source_file, enabled)",
    "values"
  );

  lines.push(
    group
      .map((row) =>
        [
          sqlString(row.category),
          sqlString(row.term),
          sqlString(row.normalizedTerm),
          sqlString(row.action),
          row.severity,
          sqlString(row.sourceFile),
          "true"
        ].join(", ")
      )
      .map((values) => `  (${values})`)
      .join(",\n") + "\n" +
      "on conflict (category, normalized_term) do update set\n" +
      "  term = excluded.term,\n" +
      "  action = excluded.action,\n" +
      "  severity = excluded.severity,\n" +
      "  source_file = excluded.source_file,\n" +
      "  enabled = excluded.enabled;"
  );
  lines.push("");
}

lines.push("commit;", "");

await writeFile(outputFile, lines.join("\n"), "utf8");

console.log(`Generated ${rows.length} sensitive terms from ${files.length} files.`);
console.log(`Output: ${outputFile}`);
console.log(`Skipped: ${JSON.stringify(skipped)}`);
